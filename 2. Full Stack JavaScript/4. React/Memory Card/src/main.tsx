import { StrictMode } from "react";
import { createRoot } from "react-dom/client";
import App from "@/components/App";

const root = document.getElementById("root");
if (root) {
  createRoot(root).render(
    <StrictMode>
      <App />
    </StrictMode>
  );
} else {
  throw new Error("Root does not exist");
}
